class Usuario:
    def __init__(self, id, nombre, apellido, email, tipo, foto, fecha_registro, contrasena):
        self.id = id
        self.nombre = nombre
        self.apellido = apellido
        self.email = email
        self.tipo = tipo
        self.foto = foto
        self.fecha_registro = fecha_registro
        self.contrasena = contrasena  
